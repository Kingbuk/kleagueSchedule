-- 팀 테이블
CREATE TABLE team (
    id            SERIAL PRIMARY KEY,
    name          VARCHAR(100) NOT NULL,
    short_name    VARCHAR(20),
    logo_url      VARCHAR(255),
    city          VARCHAR(50),
    stadium       VARCHAR(100),
    primary_color VARCHAR(7),
    league        CHAR(2) NOT NULL DEFAULT 'K1'  -- K1: K리그1, K2: K리그2
);

-- 경기장 테이블
CREATE TABLE stadium (
    id          SERIAL PRIMARY KEY,
    name        VARCHAR(100) NOT NULL,
    city        VARCHAR(50),
    address     VARCHAR(255),
    latitude    DECIMAL(10, 7),
    longitude   DECIMAL(10, 7),
    capacity    INTEGER,
    nx          INTEGER,  -- 기상청 격자 X
    ny          INTEGER   -- 기상청 격자 Y
);

-- 경기 일정 테이블
CREATE TABLE match_schedule (
    id              SERIAL PRIMARY KEY,
    round           INTEGER,
    match_date      TIMESTAMP NOT NULL,
    home_team_id    INTEGER REFERENCES team(id),
    away_team_id    INTEGER REFERENCES team(id),
    stadium_id      INTEGER REFERENCES stadium(id),
    status          VARCHAR(20) DEFAULT 'SCHEDULED',  -- SCHEDULED, LIVE, FINISHED
    home_score      INTEGER,
    away_score      INTEGER
);

